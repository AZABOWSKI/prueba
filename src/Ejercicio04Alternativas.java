
public class Ejercicio04Alternativas {
	public static void main(String[] args) {
		int numero = 10;
		//if sin rama else
		if (numero == 10) {
			System.out.println("solo con 10");
			System.out.println("solo con 10 (2)");
			System.out.println("solo con 10 (3)");
			
		}
		
		System.out.println("fin de programa");
		
		System.out.println("if con else");
		
		if (numero== 10) {
			System.out.println("solo con 10 (4)");
			System.out.println("solo con 10 (5)");
			System.out.println("solo con 10 (6)");
			
		}else { 
			System.out.println("solo distinto de 10");
			System.out.println("solo distinto de 10 (2)");
			System.out.println("solo distinto de 10 (3)");
		}
		
		System.out.println("terminado");
	}

} 
